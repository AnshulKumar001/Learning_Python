#string function

str ="my name is Anshul kumar"

#1. endswith()
print(str.endswith("Anshul"))    #false
print(str.endswith("mar"))       #true  

#2. capitalize()
print(str.capitalize())     #first word is capital

#3. replace()
print(str.replace("kumar","Rane"))      #replace word

#4. find()
print(str.find("Anshul"))       #starting index no. 

#5. count()
print(str.count("a"))  #count 

#6. upper()
print(str.upper())      #writing all word in capital  

#7. lower()
print(str.lower())          #writing all word in small